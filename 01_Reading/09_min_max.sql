-- Obtiene el valor menor del 'atributo' de la 'tabla'
Select MIN(atributo) FROM tabla;
-- Obtiene el valor menor del campo edad de la tabla "users"
Select MIN(age) FROM users;


-- Obtiene el valor mayor del 'atributo' de la 'tabla'
Select MAX(atributo) FROM tabla;
-- Obtiene el valor mayor del campo edad de la tabla "users"
Select MAX(age) FROM users;
